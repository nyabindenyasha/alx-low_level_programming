/**
 * op_add - Add two numbers.
 * @a: First number.
 * @b: Second number.
 * Return: Answer.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Substrac two numbers.
 * @a: First number.
 * @b: Second number.
 * Return: Answer.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiply two numbers.
 * @a: First number.
 * @b: Second number.
 * Return: Answer.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divide two numbers.
 * @a: First number.
 * @b: Second number.
 * Return: Answer.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Module two numbers.
 * @a: First number.
 * @b: Second number.
 * Return: Answer.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
